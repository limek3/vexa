# ClickBook TG/VK Client Menu State Machine Fix

Что исправлено:

- Telegram: выбор записи больше не зацикливается на списке. Любая кнопка `1`, `2`, `3` теперь гарантированно выбирает запись даже при устаревшем режиме в metadata.
- Telegram: режимы `choosing_chat_booking` и `choosing_action_booking` имеют приоритет над старым активным контекстом, поэтому перенос/отмена и сообщение мастеру не путаются.
- Telegram: если клиент нажал `Перенести` или `Отменить`, но активная запись не найдена, бот сразу переводит его в выбор записи для действия, а не отправляет сообщение как обычный текст.
- Telegram: карточки стали короче: компактный контекст записи и короткие инструкции.
- VK: клиентское нижнее меню переведено на callback-кнопки без отправки клиентских текстовых сообщений в чат.
- VK: выбор записи, написать мастеру, перенос/отмена и помощь работают через такую же state-machine логику, как Telegram.
- VK: режим выбора записи теперь корректно сохраняется и не сбивается старым активным контекстом.
- VK: сервисные карточки редактируются/заменяются через сохранённый id, чтобы не плодить новые сообщения.
- Общий текст карточек сделан компактнее: `Комм.` вместо длинного `Комментарий`, короткий footer, меньше простыней.

SQL не требуется — схема базы не менялась.
